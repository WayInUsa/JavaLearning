package day01_statementsAndComments;

public class EscapeSequence {
    public static void main(String[] args) {

        System.out.println("Hello world");
        System.out.println("Name \"Yurii\"");
        System.out.println("Age \'25\' ");
        System.out.println("Testing backslash \\ ");
        System.out.print("testing Spacing");
        System.out.println("\nChecking \nspacing");
        System.out.println("My name is\t\tYurii");
        System.out.println("My name is      Yurii");


    }
}
