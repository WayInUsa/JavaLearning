package day02_variables;

public class DatesVariables {

    public static void main(String[] args) {

        int month, day, year;
        month = 4;
        day = 3;
        year = 2023;

        System.out.println(month + "/" + day + "/" + year);

        month = 5;
        day = 2;

        System.out.println(month + "/" + day + "/" + year);



    }

}
