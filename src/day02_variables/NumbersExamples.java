package day02_variables;

public class NumbersExamples {

    public static void main(String[] args) {

        //byte

        byte dateNumber = 6;
        System.out.println(dateNumber);
        System.out.println("dateNumber");

        // short

        short numberTwo = 100;
        System.out.println(numberTwo);
        System.out.println("032");

        // int

        int intNumber;      // registered
        intNumber = 1000;   // add value

        //long

        long fourthNumber = 2344344;
        System.out.println(fourthNumber);

        long bingNumber = 3_000_000_000L; // make it easier to read, L need to be for code registering long
        System.out.println(bingNumber);



    }





}
