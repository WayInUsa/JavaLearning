package day03_variables;

public class CharExamples {

    public static void main(String[] args) {

        char letterOne = 'a';
        System.out.println(letterOne);
        System.out.println("\nletterOne = " + letterOne);

        char letterTwo = 'b';
        System.out.println("letterTwo = " + letterTwo);

        char letterThree = 90;      //char letterThree = 'Z'; or char letterThree = 90;
        System.out.println("letterThree = " + letterThree);

        letterThree = 121;          // char letterThree now = 121 and its 'y'
        System.out.println("Updated character: " +letterThree);

        char letterFour = 54;
        System.out.println("letterFour = " + letterFour);

    }

}
